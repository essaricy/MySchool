CREATE TABLE wavefire.ORGANIZATION_PROFILE (
    ORGANIZATION_NAME varchar(24) NOT NULL,
    CURRENT_AY_NAME varchar(16) NOT NULL,
    ADDRESS varchar(128) NOT NULL,
    PHONE_NUMBER varchar(16) NOT NULL,
    FAX_NUMBER varchar(16) NULL,
    MAP_URL varchar(512) NULL DEFAULT NULL,
    PRIMARY KEY (ORGANIZATION_NAME)
);
ALTER TABLE wavefire.ORGANIZATION_PROFILE ADD UNIQUE (ORGANIZATION_NAME);
ALTER TABLE wavefire.ORGANIZATION_PROFILE ADD CONSTRAINT FK_ACADEMICS FOREIGN KEY (CURRENT_AY_NAME) REFERENCES wavefire.ACADEMICS (ACADEMIC_YEAR_NAME);


